package util;

public class odi {
	private String fid;
	private String did;
	public String getFid() {
		return fid;
	}
	public void setFid(String fid) {
		this.fid = fid;
	}
	public String getDid() {
		return did;
	}
	public void setDid(String did) {
		this.did = did;
	}
	public odi(String fid, String did) {
		this.fid = fid;
		this.did = did;
	}
	
}
